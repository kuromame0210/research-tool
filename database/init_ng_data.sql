-- NG検知用関数の作成
CREATE OR REPLACE FUNCTION check_ng_keywords(product_title TEXT, seller_name TEXT DEFAULT '')
RETURNS TABLE(is_ng BOOLEAN, matched_keywords TEXT[]) AS $$
DECLARE
    matched_words TEXT[] := '{}';
    keyword_record RECORD;
    search_text TEXT;
BEGIN
    -- 検索対象テキストを準備
    search_text := LOWER(COALESCE(product_title, '') || ' ' || COALESCE(seller_name, ''));
    
    -- NGワードをチェック
    FOR keyword_record IN SELECT keyword FROM ng_keywords LOOP
        IF search_text LIKE '%' || LOWER(keyword_record.keyword) || '%' THEN
            matched_words := array_append(matched_words, keyword_record.keyword);
        END IF;
    END LOOP;
    
    -- 結果を返す
    RETURN QUERY SELECT 
        array_length(matched_words, 1) > 0 AS is_ng,
        matched_words;
END;
$$ LANGUAGE plpgsql;

-- NG出品者検知用関数の作成
CREATE OR REPLACE FUNCTION check_ng_seller(platform_name TEXT, seller_code TEXT)
RETURNS BOOLEAN AS $$
BEGIN
    RETURN EXISTS (
        SELECT 1 FROM ng_sellers 
        WHERE platform = platform_name AND seller_id = seller_code
    );
END;
$$ LANGUAGE plpgsql;

